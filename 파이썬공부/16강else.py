score = float(input("학점을 입력해주세요: "))


if score == 4.5 :
    print("신")
elif score >= 4.2 :
    print("교수님의 사랑")
elif score >= 3.5 :
    print("현 체제의 수호자")
elif score >= 2.8 :
    print("일반인")
elif score >= 2.3 :
    print("일탈을 꿈꾸는 소시민")
elif score >= 1.75 :
    print("오락문화의 선구자")
elif score >= 1.0 :
    print("불가촉천민")
elif score >= 0.5 :
    print("자벌레")
elif score > 0 :
    print("플랑크톤")
elif score == 0 :
    print("시대를 앞서가는 혁명의 씨앗")